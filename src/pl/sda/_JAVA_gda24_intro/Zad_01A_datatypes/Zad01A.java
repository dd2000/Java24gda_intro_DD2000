package pl.sda._JAVA_gda24_intro.Zad_01A_datatypes;

/*
  Napisz program, który utworzy dwie zmienne,
  a następnie wypisze na ekran ich sumę, różnicę i iloczyn.
*/
public class Zad01A {
    public void main(String[] args)
    {
        double a=5.6, b=4.2;
        System.out.println("Suma liczb "+a+" i "+b+ " wynosi = " +(a+b));
        System.out.println("Różnica liczb "+a+" i "+b+ " wynosi = " +(a-b));
        System.out.println("Iloczyn liczb "+a+" i "+b+ " wynosi = " +(a*b));
    } //main
} // class
